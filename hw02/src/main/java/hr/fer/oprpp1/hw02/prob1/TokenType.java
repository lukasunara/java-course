/**
 * This enumeration defines what can a token represent. 
 * 
 * @author lukasunara
 *
 */
public enum TokenType {
	
	/** Represents end of file **/
	EOF,
	
	/** Represents a word **/
	WORD,
	
	/** Represents a number **/
	NUMBER,
	
	/** Represents a single symbol **/
	SYMBOL
	
}
